#include "3-calc.h"

/**
 * op_add - 5 functions
 * @a: input one
 * @b: input two
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - 5 functions
 * @a: input one
 * @b: input two
 * Return: difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - 5 functions
 * @a: input one
 * @b: input two
 * Return: multiplication of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - 5 functions
 * @a: input one
 * @b: input two
 * Return: result of dividing a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - 5 functions
 * @a: input one
 * @b: input two
 * Return: resulting remainder modulus
 */

int op_mod(int a, int b)
{
	return (a % b);
}
